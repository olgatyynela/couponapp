export const getColor = color => {
    switch (color) {
        case 'pink':
            return '#FFCCEA'
        case 'purple':
            return '#C6ADFF'
        case 'blue':
            return '#B1DAFF'
        case 'orange':
            return '#FFB77E'
        case 'salmon':
            return '#FFCCCC'
        case 'green':
            return '#C1E1C1'
    }
}

export const getSubColor = color => {
    switch (color) {
        case 'pink':
            return '#FE85CC'
        case 'purple':
            return '#A077FF'
        case 'blue':
            return '#6DBAFF'
        case 'orange':
            return '#FF923C'
        case 'salmon':
            return '#FE8585'
        case 'green':
            return '#54AD56'
    }
}
