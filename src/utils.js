export const getColor = color => {
    switch (color) {
        case 'pink':
            return '#FFCCEA'
        case 'purple':
            return '#C6ADFF'
        case 'blue':
            return '#B1DAFF'
        case 'orange':
            return '#FFB77E'
    }
}

export const getSubColor = color => {
    switch (color) {
        case 'pink':
            return '#FE85CC'
        case 'purple':
            return '#A077FF'
        case 'blue':
            return '#6DBAFF'
        case 'orange':
            return '#FF923C'
    }
}
